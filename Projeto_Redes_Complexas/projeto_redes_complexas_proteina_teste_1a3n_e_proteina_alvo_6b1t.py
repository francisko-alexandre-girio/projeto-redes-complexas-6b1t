def plot_3d_comunidades(df_com, G, output_dir, prefix, titulo, max_edges_plot=15000):
    df_plot = df_com.copy()

    edges = list(G.edges())
    if len(edges) > max_edges_plot:
        rng = np.random.default_rng(RANDOM_STATE)
        idx = rng.choice(len(edges), size=max_edges_plot, replace=False)
        edges_plot = [edges[i] for i in idx]
    else:
        edges_plot = edges

    edge_x, edge_y, edge_z = [], [], []

    for u, v in edges_plot:
        x0, y0, z0 = G.nodes[u]["x"], G.nodes[u]["y"], G.nodes[u]["z"]
        x1, y1, z1 = G.nodes[v]["x"], G.nodes[v]["y"], G.nodes[v]["z"]

        edge_x += [x0, x1, None]
        edge_y += [y0, y1, None]
        edge_z += [z0, z1, None]

    edge_trace = go.Scatter3d(
        x=edge_x,
        y=edge_y,
        z=edge_z,
        mode="lines",
        line=dict(width=1, color="rgba(120,120,120,0.25)"),
        hoverinfo="none",
        showlegend=False
    )

    hover = (
        "Nó: " + df_plot["node_id"].astype(str) +
        "<br>Cadeia: " + df_plot["chain_id"].astype(str) +
        "<br>Resíduo: " + df_plot["residue_name"].astype(str) + " " + df_plot["residue_number"].astype(str) +
        "<br>Comunidade: " + df_plot["community"].astype(str)
    )

    node_trace = go.Scatter3d(
        x=df_plot["x"],
        y=df_plot["y"],
        z=df_plot["z"],
        mode="markers",
        marker=dict(
            size=3.5,
            color=df_plot["community"],
            colorscale="Viridis",
            opacity=0.85,
            showscale=True,
            colorbar=dict(title="Comunidade")
        ),
        hovertext=hover,
        hoverinfo="text",
        showlegend=False
    )

    fig = go.Figure(data=[edge_trace, node_trace])

    fig.update_layout(
        title=titulo,
        scene=dict(
            xaxis_title="X",
            yaxis_title="Y",
            zaxis_title="Z"
        ),
        width=950,
        height=750,
        margin=dict(l=0, r=0, b=0, t=40)
    )

    html_path = os.path.join(output_dir, f"{prefix}_3d_comunidades.html")
    fig.write_html(html_path)

    try:
        png_path = os.path.join(output_dir, f"{prefix}_3d_comunidades.png")
        fig.write_image(png_path, scale=2)
        print("Figura 3D PNG salva em:", png_path)
    except Exception as e:
        png_path = None
        print("Não foi possível salvar PNG estático da figura 3D. HTML salvo normalmente.")
        print("Motivo:", e)

    fig.show()
    print("Figura 3D HTML salva em:", html_path)

    return html_path, png_path


