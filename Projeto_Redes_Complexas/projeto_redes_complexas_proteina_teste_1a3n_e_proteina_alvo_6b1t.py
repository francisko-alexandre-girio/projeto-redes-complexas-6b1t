def calcular_centralidades(G, output_dir, prefix, betweenness_k=None):
    print("Calculando centralidade de grau...")
    degree = nx.degree_centrality(G)

    closeness = {}
    eigenvector = {}
    pagerank = {}
    betweenness = {}

    if CALCULAR_CLOSENESS:
        print("Calculando Closeness...")
        closeness = nx.closeness_centrality(G)

    if CALCULAR_EIGENVECTOR:
        print("Calculando Eigenvector...")
        try:
            eigenvector = nx.eigenvector_centrality(G, max_iter=1000, tol=1e-06)
        except Exception as e:
            print("Eigenvector não convergiu:", e)
            eigenvector = {n: np.nan for n in G.nodes()}

    if CALCULAR_PAGERANK:
        print("Calculando PageRank...")
        pagerank = nx.pagerank(G, alpha=0.85, max_iter=200, weight="weight")

    print("Calculando Betweenness...")
    if betweenness_k is None:
        betweenness = nx.betweenness_centrality(G, normalized=True, weight=None)
    else:
        betweenness = nx.betweenness_centrality(
            G,
            k=betweenness_k,
            normalized=True,
            seed=RANDOM_STATE,
            weight=None
        )

    dados = []
    for n in G.nodes():
        attrs = G.nodes[n]
        dados.append({
            "node_id": n,
            "chain_id": attrs.get("chain_id"),
            "residue_name": attrs.get("residue_name"),
            "residue_number": attrs.get("residue_number"),
            "grau": G.degree(n),
            "degree_centrality": degree.get(n, np.nan),
            "closeness": closeness.get(n, np.nan),
            "eigenvector": eigenvector.get(n, np.nan),
            "pagerank": pagerank.get(n, np.nan),
            "betweenness": betweenness.get(n, np.nan),
            "x": attrs.get("x"),
            "y": attrs.get("y"),
            "z": attrs.get("z"),
        })

    df_cent = pd.DataFrame(dados)

    df_cent.to_csv(
        os.path.join(output_dir, f"{prefix}_centralidades.csv"),
        index=False,
        encoding="utf-8-sig"
    )

    for col in ["grau", "degree_centrality", "closeness", "eigenvector", "pagerank", "betweenness"]:
        if col in df_cent.columns:
            top = df_cent.sort_values(col, ascending=False).head(20)

            top.to_csv(
                os.path.join(output_dir, f"{prefix}_top20_{col}.csv"),
                index=False,
                encoding="utf-8-sig"
            )

            print(f"\nTop 20 - {col}")
            display(top[["node_id", "chain_id", "residue_name", "residue_number", col]].round(6))

    return df_cent


