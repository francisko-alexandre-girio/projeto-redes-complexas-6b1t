def limpar_diretorio(path):
    os.makedirs(path, exist_ok=True)
    for item in os.listdir(path):
        full = os.path.join(path, item)
        if os.path.isfile(full) or os.path.islink(full):
            os.remove(full)
        elif os.path.isdir(full):
            shutil.rmtree(full)


def baixar_pdb(pdb_id, output_dir):
    """Baixa um arquivo PDB simples diretamente do RCSB."""
    os.makedirs(output_dir, exist_ok=True)
    url = f"https://files.rcsb.org/download/{pdb_id}.pdb"
    path = os.path.join(output_dir, f"{pdb_id}.pdb")
    print(f"Baixando {pdb_id} do RCSB...")
    urllib.request.urlretrieve(url, path)
    print("Arquivo salvo em:", path)
    return path


def carregar_residuos_ca(pdb_files, pdb_id, max_residues=None):
    """Carrega resíduos usando o átomo Cα como representante espacial."""
    parser = PDBParser(QUIET=True)
    residuos = []
    node_id = 0

    if isinstance(pdb_files, str):
        pdb_files = [pdb_files]

    for pdb_file in pdb_files:
        print("Lendo:", pdb_file)
        estrutura = parser.get_structure(pdb_id, pdb_file)

        for modelo in estrutura:
            for cadeia in modelo:
                for residuo in cadeia:
                    if "CA" not in residuo:
                        continue

                    atom_ca = residuo["CA"]
                    res_id = residuo.get_id()

                    residuos.append({
                        "node_id": node_id,
                        "pdb_id": pdb_id,
                        "source_file": os.path.basename(pdb_file),
                        "model_id": modelo.id,
                        "chain_id": str(cadeia.id),
                        "residue_name": residuo.get_resname(),
                        "residue_number": int(res_id[1]),
                        "insertion_code": res_id[2].strip() if res_id[2].strip() else "",
                        "x": float(atom_ca.coord[0]),
                        "y": float(atom_ca.coord[1]),
                        "z": float(atom_ca.coord[2])
                    })

                    node_id += 1
                    if max_residues is not None and node_id >= max_residues:
                        return pd.DataFrame(residuos)

    return pd.DataFrame(residuos)


def construir_rede_por_distancia(df_residuos, distance_threshold=8.0):
    """Constrói grafo não direcionado usando KDTree para pares Cα-Cα dentro do limiar."""
    coords = df_residuos[["x", "y", "z"]].values
    tree = cKDTree(coords)
    pares = sorted(tree.query_pairs(r=distance_threshold))

    G = nx.Graph()

    for _, row in df_residuos.iterrows():
        G.add_node(
            int(row["node_id"]),
            pdb_id=row["pdb_id"],
            source_file=row["source_file"],
            chain_id=row["chain_id"],
            residue_name=row["residue_name"],
            residue_number=int(row["residue_number"]),
            insertion_code=row["insertion_code"],
            x=float(row["x"]),
            y=float(row["y"]),
            z=float(row["z"])
        )

    for i, j in pares:
        dist = float(np.linalg.norm(coords[i] - coords[j]))
        G.add_edge(int(i), int(j), distance=dist, weight=1.0)

    return G


def metricas_rede(G):
    graus = dict(G.degree())
    componentes = list(nx.connected_components(G))
    maior_comp = max(componentes, key=len) if componentes else []

    return pd.DataFrame({
        "Métrica": [
            "Nós", "Arestas", "Densidade", "Grau médio", "Grau máximo",
            "Coeficiente de agrupamento médio", "Componentes conectados", "Maior componente"
        ],
        "Valor": [
            G.number_of_nodes(),
            G.number_of_edges(),
            nx.density(G),
            np.mean(list(graus.values())) if graus else 0,
            max(graus.values()) if graus else 0,
            nx.average_clustering(G) if G.number_of_nodes() > 0 else 0,
            len(componentes),
            len(maior_comp)
        ]
    })


