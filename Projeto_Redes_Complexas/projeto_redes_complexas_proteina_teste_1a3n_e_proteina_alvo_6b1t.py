def detectar_comunidades_louvain(G, output_dir, prefix, resolution=1.0):
    print("Aplicando Louvain...")

    # Correção principal:
    # o python-louvain exige que o parâmetro weight seja uma string.
    # Portanto, usamos o atributo "weight" das arestas.
    for u, v, data in G.edges(data=True):
        data["weight"] = float(data.get("weight", 1.0))

    partition = community_louvain.best_partition(
        G,
        weight="weight",
        resolution=resolution,
        random_state=RANDOM_STATE
    )

    nx.set_node_attributes(G, partition, "community")

    modularidade = community_louvain.modularity(
        partition,
        G,
        weight="weight"
    )

    num_comunidades = len(set(partition.values()))

    df_com = pd.DataFrame({
        "node_id": list(partition.keys()),
        "community": list(partition.values())
    })

    for attr in ["chain_id", "residue_name", "residue_number", "source_file", "x", "y", "z"]:
        df_com[attr] = df_com["node_id"].map(lambda n: G.nodes[n].get(attr))

    df_com.to_csv(
        os.path.join(output_dir, f"{prefix}_comunidades_louvain.csv"),
        index=False,
        encoding="utf-8-sig"
    )

    resumo = (
        df_com.groupby("community")
        .size()
        .reset_index(name="quantidade_residuos")
        .sort_values("quantidade_residuos", ascending=False)
    )

    resumo.to_csv(
        os.path.join(output_dir, f"{prefix}_resumo_comunidades.csv"),
        index=False,
        encoding="utf-8-sig"
    )

    print(f"Número de comunidades: {num_comunidades}")
    print(f"Modularidade: {modularidade:.4f}")

    display(resumo)

    return partition, modularidade, num_comunidades, df_com, resumo


def plot_heatmap_crosstab(tabela, output_dir, filename, titulo, xlabel, ylabel):
    plt.figure(figsize=(max(8, 0.45 * tabela.shape[1] + 4), max(5, 0.35 * tabela.shape[0] + 3)))
    plt.imshow(tabela.values, aspect="auto")
    plt.colorbar(label="Quantidade de resíduos")
    plt.xticks(range(tabela.shape[1]), tabela.columns, rotation=45, ha="right")
    plt.yticks(range(tabela.shape[0]), tabela.index)
    plt.xlabel(xlabel)
    plt.ylabel(ylabel)
    plt.title(titulo)

    if tabela.shape[0] * tabela.shape[1] <= 250:
        for i in range(tabela.shape[0]):
            for j in range(tabela.shape[1]):
                valor = tabela.values[i, j]
                if valor != 0:
                    plt.text(j, i, str(valor), ha="center", va="center", fontsize=7)

    plt.tight_layout()
    path = os.path.join(output_dir, filename)
    plt.savefig(path, dpi=300)
    plt.show()

